pub(crate) fn material_file_icon_path(path: &str) -> Option<&'static str> {
    let normalized_path = normalize_file_path(path);
    if normalized_path.is_empty() {
        return None;
    }

    if let Some(icon_path) = lookup(FILE_NAMES, &normalized_path) {
        return Some(icon_path);
    }

    if let Some(icon_path) = lookup_file_path_suffix(&normalized_path) {
        return Some(icon_path);
    }

    let file_name = normalized_path
        .rsplit('/')
        .next()
        .unwrap_or(normalized_path.as_str());
    if file_name != normalized_path.as_str()
        && let Some(icon_path) = lookup(FILE_NAMES, file_name)
    {
        return Some(icon_path);
    }

    extension_icon_path(file_name)
}

fn extension_icon_path(file_name: &str) -> Option<&'static str> {
    if let Some(icon_path) = lookup(FILE_EXTENSIONS, file_name) {
        return Some(icon_path);
    }

    for (index, character) in file_name.char_indices() {
        if character != '.' || index + 1 >= file_name.len() {
            continue;
        }

        if let Some(icon_path) = lookup(FILE_EXTENSIONS, &file_name[index + 1..]) {
            return Some(icon_path);
        }
    }

    None
}

fn lookup_file_path_suffix(path: &str) -> Option<&'static str> {
    FILE_PATH_SUFFIXES.iter().find_map(|(suffix, icon_path)| {
        if path == *suffix {
            return Some(*icon_path);
        }

        let prefix_length = path.len().checked_sub(suffix.len())?;
        if prefix_length == 0 || !path.ends_with(suffix) {
            return None;
        }

        path.as_bytes()
            .get(prefix_length - 1)
            .is_some_and(|character| *character == b'/')
            .then_some(*icon_path)
    })
}

fn lookup(entries: &'static [(&'static str, &'static str)], key: &str) -> Option<&'static str> {
    entries
        .binary_search_by(|(entry, _)| entry.cmp(&key))
        .ok()
        .map(|index| entries[index].1)
}

fn normalize_file_path(path: &str) -> String {
    path.trim().replace('\\', "/").to_ascii_lowercase()
}

#[cfg(test)]
mod tests {
    use super::material_file_icon_path;

    #[test]
    fn resolves_language_file_extensions() {
        assert_eq!(
            material_file_icon_path("crates/ui/src/lib.rs"),
            Some("icons/material/rust.svg")
        );
        assert_eq!(
            material_file_icon_path("Sources/AppDelegate.swift"),
            Some("icons/material/swift.svg")
        );
    }

    #[test]
    fn prefers_specific_names_and_multipart_extensions() {
        assert_eq!(
            material_file_icon_path("README.md"),
            Some("icons/material/readme.svg")
        );
        assert_eq!(
            material_file_icon_path("src/types.d.ts"),
            Some("icons/material/typescript-def.svg")
        );
        assert_eq!(
            material_file_icon_path("packages/app/.config/prettierrc"),
            Some("icons/material/prettier.svg")
        );
    }

    #[test]
    fn returns_none_without_a_theme_association() {
        assert_eq!(material_file_icon_path("artifact.unknown-ext"), None);
    }
}
